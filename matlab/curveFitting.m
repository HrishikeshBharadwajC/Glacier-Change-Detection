function c = curveFit(x,y,name)
    y = 70 - y;
    p = polyfit(x,y,2);
    f = polyval(p,x);
    figure(1);
    plot(x,f,"DisplayName", name);
    axis([0 190 0 70]);
    c = trapz(x,y);
    return
end